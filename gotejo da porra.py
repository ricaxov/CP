''' Gotejo da porra (Penis Virtual Edition) '''
import turtle

t = turtle.Turtle()
t.setx(0)
t.penup()
t.goto(0,-200)
t.pendown()
t.sety(-300)
t.circle(100,90)
t.sety(-200)
t.setx(100)
t.setx(-100)
t.circle(-100,-90)
t.penup()
t.goto(100,-200)
t.pendown()
t.goto(100,150)
t.circle(-100)
t.goto(-100,150)
t.circle(-100)
t.goto(-100,-200)
turtle.done()
